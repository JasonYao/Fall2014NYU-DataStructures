/**
 * An exception class to be thrown when there is an issue evaluating a prefix expression
 * @author Jason
 */
public class PreFixException extends Exception
{
	private static final long serialVersionUID = -2570703168816013273L;

	public PreFixException()
	{super();}
	
	public PreFixException(String message)
	{super(message);}	
} // End of the PreFixException class
